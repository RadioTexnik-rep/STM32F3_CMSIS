static int temp = 0;

//----------------------------------------------------/
//                		Main
//----------------------------------------------------/
int main(void)
{ 
    vInitGPIO();
    temp = iInitClock();

    while (1)
    {
        
        if (temp)
        {
            LED_ON;
        }else{
            LED_OFF;
        } 
           
        PB15_TOGGLE;
        
    }
    //return 0;
}


